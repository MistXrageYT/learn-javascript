//what is scope?
//answer: scope is the area that a variable can be used in.

//what is global scope?
//answer: global scope is the scope that is available throughout the entire code.

//what is local scope?
//answer: local scope is the scope that is available within a function.

//what is block scope?
//answer: block scope is the scope that is available within a block of code.

//which variable is used for block and local scope?
//answer: let and const

//why is let and const is better choice?
//answer: let and const is better choice because they are block scoped.

//why not var?
//answer: var is better choice because it is global scoped

//which should we use?
//answer: let and const
